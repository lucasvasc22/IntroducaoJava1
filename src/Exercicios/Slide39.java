package Exercicios;

public class Slide39 {

	public static void main(String[] args) {
		
		//Controles de Fluxo do programa - switch()
		int numero = 1;
		switch(numero){
		case 1 :
			System.out.println( "Um" );
			break;
		case 2 :
			System.out.println( "DOIS" );
			break;
		case 3 : 
			System.out.println( "TRÊS ");
			break;
		default : 
			System.out.println( "NENHUM" );
			break;
		}
	}

}
